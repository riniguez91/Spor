package com.example.spor_tfg

class UserHelperClass {

    var name: String = "";
    var username: String = "";
    var email: String = "";
    var phoneNo: String= "";
    var password: String= "";

    constructor() { }

    constructor(name: String, username: String, email: String, phoneNo: String, password: String) {
        this.name = name
        this.username = username
        this.email = email
        this.phoneNo = phoneNo
        this.password = password
    }




}